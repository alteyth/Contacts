package Rubrica;

public class Persona {
    String nome;
    String cognome;
    String indirizzo;
    String telefono;
    int eta;

    public Persona(String nome, String cognome, String indirizzo, String telefono, int eta) {
        this.nome = nome;
        this.cognome = cognome;
        this.indirizzo = indirizzo;
        this.telefono = telefono;
        this.eta = eta;
    }
}
